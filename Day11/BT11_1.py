# Định nghĩa 4 hàm add, subtract, divide, multiply (+, -, /, *).
# Mỗi hàm nhận vào hai tham số thực hiện lần lượt các phép toán cộng, trừ, chia, nhân. Lưu ý hàm nên trả về thay vì in ra.

# add
def my_add(x, y):
    return (x + y)


print(my_add(6, 3))

# subtract


def my_subtract(a, b):
    return (a-b)


print(my_subtract(6, 3))

# divide


def my_subtract(c, d):
    return (c/d)


print(my_subtract(6, 3))

# multiply


def my_subtract(e, f):
    return (e*f)


print(my_subtract(6, 3))
